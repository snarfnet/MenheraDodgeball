    // MARK: - ランダム取得
    static func random(_ category: DialogueCategory) -> String {
        let pool: [String]
        switch category {
        case .attack: pool = attack
        case .yami: pool = yami
        case .tsundere: pool = tsundere
        case .winning: pool = winning
        case .losing: pool = losing
        case .hitEnemy: pool = hitEnemy
        case .gotHit: pool = gotHit
        case .dodge: pool = dodge
        case .pickup: pool = pickup
        case .taunt: pool = taunt
        case .lastOne: pool = lastOne
        case .roundStart: pool = roundStart
        case .victory: pool = victory
        case .defeat: pool = defeat
        }
        return pool.randomElement() ?? "..."
    }

    /// 投げるとき用：攻撃的/病み/ツンデレからランダムに選ぶ
    static func randomThrow() -> String {
        let allThrow = attack + yami + tsundere
        return allThrow.randomElement() ?? "..."
    }

    /// 状況に応じた投げセリフ（スコア差で変化）
    static func throwDialogue(myScore: Int, enemyScore: Int) -> String {
        if myScore > enemyScore + 1 {
            return [winning, attack, tsundere].flatMap { $0 }.randomElement() ?? "..."
        } else if enemyScore > myScore + 1 {
            return [losing, yami].flatMap { $0 }.randomElement() ?? "..."
        } else {
            return randomThrow()
        }
    }
